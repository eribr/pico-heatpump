    def _send_pulses(self, pulse_list):
        self.pi.wave_clear()
        wave = []
        for i, us in enumerate(pulse_list):
            if i % 2 == 0: # Mark
                cycles = int(us / 26)
                for _ in range(cycles):
                    wave.append(pigpio.pulse(1<<self.ir_pin, 0, 13))
                    wave.append(pigpio.pulse(0, 1<<self.ir_pin, 13))
            else: # Space
                wave.append(pigpio.pulse(0, 1<<self.ir_pin, us))

        self.pi.wave_add_generic(wave)
        wid = self.pi.wave_create()
        if wid >= 0:
            self.pi.wave_send_once(wid)
            while self.pi.wave_tx_busy():
                time.sleep(0.01)
            self.pi.wave_delete(wid)
            return True
        return False

    def set_temperature(self, temp):
        if temp not in self.config:
            print(f"Temperature {temp} is not supported.")
            return False

        c = self.config[temp]
        base_pulses = list(self.masters[c["base"]])
        
        # Apply bit flips on the copy
        for bit_idx in c["flips"]:
            pulse_idx = (bit_idx * 2) + 3
            if pulse_idx < len(base_pulses):
                # Flip between 0 (~850us) and 1 (~2140us)
                base_pulses[pulse_idx] = 2140 if base_pulses[pulse_idx] < 1500 else 850

        print(f"Sending {temp}°C (Based on master {c['base']})")
        return self._send_pulses(base_pulses)

    def power_on(self):
        print("Sending Power ON command...")
        return self._send_pulses(self.masters["on"])

    def power_off(self):
        print("Sending Power OFF command...")
        return self._send_pulses(self.masters["off"])

    def set_fan_auto(self):
        print("Sending Fan AUTO command...")
        return self._send_pulses(self.masters["auto"])

    def close(self):
        self.pi.stop()

